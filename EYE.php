        <h1 class="usos">¿QUE ES LA ELECTRONICA Y PARA QUE SIRVE?</h1>
        <br><br>
        
        <div class="elec">
            <p>La mejor forma de comenzar en la electrónica será centrarse en sus propósitos o funciones. La electrónica sirve principalmente para confeccionar y diseñar todo tipo de aparatos electrónicos como computadoras, celulares, relojes digitales, televisores, circuitos electrónicos, entre muchos otros. Todos estos parten de la electrónica básica, por lo que sin esta disciplina no podría existir nada que necesite de una corriente eléctrica para su funcionamiento.</p>

De igual manera, la electrónica sirve y mejora la función de otras disciplinas como las telecomunicaciones y la robótica. El óptimo desarrollo de la electrónica permite mejorar la capacidad tecnológica de cualquier objeto o aparato que usemos en la vida diaria.  
            <br><br>
            <h1>ELEMENTOS Y CARACTERISTICAS DE LA ELECTRONICA</h1>
            <br><br>
            <p>
                La electrónica no podría existir sin una serie de elementos que permiten el funcionamiento correcto de esta disciplina. Conviértete en un profesional en este campo con nuestro Diplomado en Reparación Electrónica. Deja que nuestros docentes y expertos te ayuden a emprender desde el primer momento.
                
                <un>
                <li>Circuito electrónico: El circuito electrónico es una placa compuesta por diversos elementos semiconductores tanto pasivos como activos por los que fluye la corriente eléctrica. La función de un circuito electrónico es generar, transmitir, recibir y almacenar información; sin embargo, y de acuerdo a su función, estos propósitos pueden cambiar.</li>
                <li>Circuitos integrados: Es un circuito de tamaño minúsculo en el que se instalan diversas piezas electrónicas. Generalmente está dentro de un encapsulado de plástico o cerámica que permite salvaguardar su estructura. Estos dispositivos son usados en aparatos como electrodomésticos, dispositivos del campo de la salud, belleza, mecánica, entre otros.</li>
                <li>Resistencias: Una resistencia es un dispositivo cuya principal función es dificultar el paso de la corriente eléctrica. Estas cuentan con una escala de valores que le permiten adecuarse a las necesidades que se requieran. </li>
                <li>Diodos: Caso contrario a las resistencias, los diodos funcionan como una vía por la que corre la energía eléctrica en un solo sentido. Cuenta con diversas variedades como el rectificador, zener, fotodiodo, entre otros.</li>
                <li>Transistores: Es uno de los componentes más importantes de la electrónica en general. Consiste en un dispositivo semiconductor que sirve para entregar una señal de salida en respuesta a una de entrada. En pocas palabras, es un pequeño interruptor que sirve para encender, apagar y amplificar corrientes eléctricas.</li>
                <li>Microcontroladores: Son un tipo de circuito integrado programable en el que se graban acciones ejecutadas de manera manual o automática. Se encuentran en un sinfín de dispositivos como juguetes, ordenadores, electrodomésticos y hasta automóviles. </li>
                <li>Condensadores o capacitores: Es un dispositivo utilizado para almacenar energía eléctrica en un campo eléctrico. Cuenta con una variedad de tamaños además de ser construido con varios materiales dieléctricos como la cerámica, polietileno, glass, mica, óxido de aluminio, entre otros. </li>
                </un>
            </p>
