        <h1 class="tcq">9 ETAPAS DEL DESARROLLO WEB</h1><br><br>
        <div class="ddd">
            <p>
                <un>
            <li>1. Define el perfil del proyecto<br>
Para iniciar un sitio web lo primero es establecer las bases de lo que se requiere, es decir definir cuál será el objetivo del proyecto, a quiénes va dirigido, para qué es necesario en el negocio o la empresa, qué se podrá hacer o consultar, cuál es la personalidad de la marca que se quiere mostrar, entre otros aspectos esenciales.</li><br>

<li>2. Planifica el trabajo<br>
En este momento debes recopilar toda la información necesaria para fijar cada una de las etapas del proyecto. Involucra la investigación de mercado, el análisis de la competencia y el estudio de los consumidores. A partir de ello decide cuál es la estrategia de contenido y la estructura del sitio web más adecuadas para el propósito del proyecto.</li><br>

<li>3. Diseña el sitio web<br>
Esta etapa corresponde a la organización y acomodo de los elementos que van a componer cada una de las páginas del sitio, es decir, el diseño web. Con la participación del cliente y atendiendo la voz de marca, hay que seleccionar los colores que se usarán; la posición del logotipo; el tipo de letra para barras, títulos, menús y cuerpos de texto; las imágenes y videos; la versión móvil y para otros dispositivos, y las principales interacciones que tendrá el sitio.</li><br>
                    
<li>4. Determina los contenidos<br>
Es importante definir los contenidos del sitio web, atendiendo aspectos SEO para todo lo relacionado con banners, bloques de información y los textos de cada una de las secciones. Este trabajo se relaciona estrechamente con el punto anterior, ya que el diseño y los contenidos se conjugan para que sea funcional.</li><br>

<li>5. Desarrolla el código<br>
Al tener en cuenta lo anterior, es momento para la traducción a un lenguaje de programación y frameworks. Inicia con el wireframe o esquema de las páginas del sitio para adaptarlo. En este punto también se determinan las funcionalidades que tendrá el sitio, la configuración del SEO, la base de datos, , la velocidad de carga y rendimiento, las pruebas del código, la ciberseguridad, entre otros aspectos relacionados. Recuerda que puedes usar recursos como React.</li><br>

En este punto del desarrollo web hay un trabajo por parte del backend y otra del frontend para que haya una funcionalidad eficiente, tanto para el servidor como la interfaz y la interacción de todos los elementos que integran el sitio web.

<li>6. Revisa y comprueba el funcionamiento del sitio<br>
Cuando ya se ha generado la programación debe haber una revisión exhaustiva del funcionamiento del sitio web, atendiendo a lo que se esquematizó en diseño y revisando que los contenidos estén correctos y operen adecuadamente.</li><br>

<li>7. Sube el sitio web<br>
Cuando ya verificaste la programación del sitio y ya se ha comprobado su funcionamiento, diseño y contenidos es momento del lanzamiento de la página web, es decir, ponerla en marcha en la red.</li><br>

Entrega los archivos para que sean publicados en el servidor elegido por el cliente. Luego de ello, los internautas ya podrán acceder al sitio oficialmente. Si es una web de negocios es de suma importancia que agregues un software de analítica para obtener las métricas e informes del comportamiento de los usuarios; sobre todo para acciones de compra, relleno de formularios, chats, etc. 

<li>8. Evalúa y optimiza<br>
Analiza cómo está funcionando el sitio web una vez que ya está en la red, verifica áreas de mejora y trata de optimizar todo lo que puedas en cualquier nivel: ya sea en el backend o en el frontend. Incluso esta evaluación te sirve para identificar puntos de diseño o contenidos que se pueden mejorar.</li> 
<br>
<li>9. Haz un mantenimiento continuo<br>
Es una de las acciones vitales, pues de esto depende la permanencia de un sitio en internet. Si no atiendes los errores en el sitio que puedan surgir, es posible que muchos usuarios ya no regresen por la mala experiencia de navegación. Así que mantente al tanto del funcionamiento óptimo de todas las páginas de la aplicación web.</li><br>
            </un></p>
