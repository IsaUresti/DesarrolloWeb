        <h1 class="udt">¿QUE ES EL DESARROLLO WEB?</h1><br><br>
        
        <div class="ccs">
        <p>Se conoce como desarrollo web al proceso de crear y mantener un sitio web que sea funcional en internet, a través de diferentes lenguajes de programación, según el modelo y la parte de la página que corresponda. Cada sitio tiene una URL única que lo distingue de los demás en la red informática mundial.
                
        Un sitio web puede clasificarse de diferentes formas. Para cuestiones de desarrollo web principalmente se divide en dos partes. 
        </p><br><br>
        
            <un>
            <li>Frontend. Es la parte que interactúa con el usuario, tanto en imagen como en función. Por ello está íntimamente relacionada con la experiencia del usuario (UX) y la interfaz de usuario (IU).</li> 
            <li>Backend. Se refiere a la parte que entra en contacto directo con el servidor; es donde se aplica el código de programación para crear la estructura. Permanece en un segundo plano a cargo de la accesibilidad, actualización, bases de datos y cambios del sitio.</li>
            </un><br><br>
        </div>
        <img id="daw" src="daw.jpg" height="500vh"><br><br>
        
        <h1 class="son">DIFERENCIA ENTRE DISEÑO Y DESARROLLO WEB</h1><br><br>
        
        <div class="dod">
            <p>Una vez definido el frontend y el backend es más sencillo distinguir la diferencia entre ambos conceptos. El diseño web está relacionado con el frontend, pues se encarga de definir la apariencia estética del sitio web. En específico aspectos como la imagen, gama de colores, cómo se verá el contenido, incluyendo texto, videos, fotografías, iconos, botones, menús, barras y demás elementos. Su objetivo es que el sitio sea fácil de entender y usar.<br><br>
                
            Por otro lado, el desarrollo web va de la mano del backend. Se asegura que el código que sostiene la estructura sea funcional y a su vez lógico. También se hace cargo de la gestión de contenido y la base de datos de la web. Su objetivo es que el sitio responda correctamente a las interacciones que realice el usuario en él.<br><br>
                
            El diseño web corresponde a un sentido visual, ya que define la apariencia de cada una de las páginas que formen un sitio web. Por su parte, el desarrollo web funge como un traductor del diseño para configurarlo en un lenguaje que entienda la red. El desarrollo web se enfoca en lo funcional; podrías tener un sitio navegable y útil aunque podría carecer de estética visual; por eso es tan importante que estas dos actividades se conjuguen. </p><br><br>
        </div>
